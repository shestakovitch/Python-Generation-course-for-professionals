class Alphabet:
    def __init__(self, language):
        self.language = {'en': [chr(i) for i in range(97, 123)], 'ru': [chr(i) for i in range(1072, 1104)]}[language]
        self.index = -1

    def __iter__(self):
        return self

    def __next__(self):
        self.index += 1
        try:
            return self.language[self.index]
        except IndexError:
            self.index = 0
            return self.language[self.index]